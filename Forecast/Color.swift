import Foundation
import UIKit

extension UIColor {
  
    class func pastelRed() -> UIColor {
        
        return UIColor(red: 255.0/255.0, green: 179.0/255.0, blue: 71.0/255.0, alpha: 1.0)
        
    }

    class func pastelGreen() -> UIColor {
        
        return UIColor(red: 174.0/255.0, green: 198.0/255.0, blue: 207.0/255.0, alpha: 1.0)
        
    }

}
